# Start NSO nodes
start_nso_nodes() {
    if [[ "$NO_NSO" == "true" ]]; then
        log_info "Skipping NSO start (--no-nso flag set)"
        return
    fi
    
    log_info "Starting NSO nodes..."
    
    if [[ -n "$SPECIFIC_NODE" ]]; then
        # Start specific node
        start_nso_node "$SPECIFIC_NODE"
        log_info "Started NSO node $SPECIFIC_NODE"
    else
        # Start all nodes
        for ((i=1; i<=NODES; i++)); do
            start_nso_node "$i"
        done
        log_info "All NSO nodes started"
    fi
}

# Stop a single NSO node
stop_nso_node() {
    local node_id="$1"
    local node_ip="${NETWORK_PREFIX}.${node_id}.1"
    local netns="${PREFIX}${node_id}ns"
    local env_source=$(get_nso_env_source)
    
    log_debug "Stopping NSO node $node_id"
    
    # Try to stop gracefully first (with environment sourced)
    execute_cmd "sudo ip netns exec $netns bash -c '
        $env_source
        export NCS_IPC_ADDR=$node_ip
        ncs --stop
    '" || true
    
    # Wait a moment for graceful shutdown
    sleep 2
    
    # Force kill if still running
    local pids=$(sudo ip netns exec "$netns" pgrep -f "beam.*ncs" 2>/dev/null || true)
    if [[ -n "$pids" ]]; then
        log_debug "Force killing NSO processes for node $node_id: $pids"
        echo "$pids" | xargs -r sudo ip netns exec "$netns" kill -9 || true
    fi
}

# Stop NSO nodes
stop_nso_nodes() {
    log_info "Stopping NSO nodes..."
    
    local stop_failed=false
    
    if [[ -n "$SPECIFIC_NODE" ]]; then
        if ! stop_nso_node "$SPECIFIC_NODE"; then
            log_warn "Failed to stop NSO node $SPECIFIC_NODE"
            stop_failed=true
        else
            log_info "Stopped NSO node $SPECIFIC_NODE"
        fi
    else
        for ((i=1; i<=NODES; i++)); do
            if ! stop_nso_node "$i"; then
                log_warn "Failed to stop NSO node $i"
                stop_failed=true
            fi
        done
        
        if [[ "$stop_failed" == "true" ]]; then
            log_warn "Some NSO nodes failed to stop cleanly"
        else
            log_info "All NSO nodes stopped"
        fi
    fi
    
    return 0  # Always return success to allow cleanup to continue
}

# Cleanup NSO configuration directories
cleanup_nso_nodes() {
    if [[ "$KEEP_CONFIG" == "true" ]]; then
        log_info "Keeping NSO configuration directories"
        return 0
    fi
    
    log_info "Cleaning up NSO configuration directories..."
    
    local cleanup_failed=false
    
    for ((i=1; i<=NODES; i++)); do
        local node_dir="${WORK_DIR}/ncs-run${i}"
        if [[ -d "$node_dir" ]]; then
            if ! execute_cmd "rm -rf $node_dir"; then
                log_warn "Failed to remove NSO directory: $node_dir"
                cleanup_failed=true
            fi
        fi
    done
    
    if [[ "$cleanup_failed" == "true" ]]; then
        log_warn "Some NSO directories could not be removed"
        return 1
    fi
    
    return 0
}

# Check NSO node status
check_nso_node_status() {
    local node_id="$1"
    local node_ip="${NETWORK_PREFIX}.${node_id}.1"
    local netns="${PREFIX}${node_id}ns"
    local env_source=$(get_nso_env_source)
    
    # Check if NSO process is running
    local pids=$(sudo ip netns exec "$netns" pgrep -f "beam.*ncs" 2>/dev/null || true)
    if [[ -z "$pids" ]]; then
        echo "STOPPED"
        return
    fi
    
    # Try to get RAFT role
    local role=$(sudo ip netns exec "$netns" bash -c "
        $env_source
        export NCS_IPC_ADDR=$node_ip
        timeout 5 ncs_cmd -I -c 'mget /ha-raft/status/role' 2>/dev/null || echo 'unknown'
    ")
    
    echo "RUNNING ($role)"
}

# Show NSO cluster status
show_nso_status() {
    if [[ "$NO_NSO" == "true" ]]; then
        echo "  NSO: DISABLED"
        return
    fi
    
    echo "  NSO Nodes:"
    for ((i=1; i<=NODES; i++)); do
        local node_dir="${WORK_DIR}/ncs-run${i}"
        local status="NOT_CONFIGURED"
        
        if [[ -d "$node_dir" ]]; then
            status=$(check_nso_node_status "$i")
        fi
        
        echo "    Node $i: $status"
    done
    
    # Try to show leader information
    echo ""
    echo "  RAFT Status:"
    local env_source=$(get_nso_env_source)
    for ((i=1; i<=NODES; i++)); do
        local node_ip="${NETWORK_PREFIX}.${i}.1"
        local netns="${PREFIX}${i}ns"
        
        local leader=$(sudo ip netns exec "$netns" bash -c "
            $env_source
            export NCS_IPC_ADDR=$node_ip
            timeout 5 ncs_cmd -I -c 'mget /ha-raft/status/leader' 2>/dev/null || echo 'unknown'
        " 2>/dev/null || echo "unknown")
        
        if [[ "$leader" != "unknown" ]] && [[ "$leader" != "error" ]]; then
            echo "    Leader (from node $i): $leader"
            break
        fi
    done
}

