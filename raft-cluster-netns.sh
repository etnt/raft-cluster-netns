# Setup NSO node directory and configuration
setup_nso_node() {
    local node_id="$1"
    local node_dir="${WORK_DIR}/ncs-run${node_id}"
    local node_ip
    
    # Use L3BGP node IP if configured, otherwise fall back to simple addressing
    if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
        node_ip=$(eval echo "\$NODE_${node_id}_IP")
        if [[ -z "$node_ip" ]]; then
            node_ip="${NETWORK_PREFIX}.${node_id}.1"  # fallback
        fi
    else
        node_ip="${NETWORK_PREFIX}.${node_id}.1"
    fi
    
    local node_address="ncsd${node_id}@${PREFIX}${node_id}.ha-cluster"
    local seed_address=$(get_seed_node_address "$node_id")
    local env_source=$(get_nso_env_source)

    log_debug "Setting up NSO node $node_id in $node_dir"
    log_debug "ENV_SH_PATH: '$ENV_SH_PATH'"
    log_debug "env_source: '$env_source'"
    
    # Ensure NSO environment is available
    if [[ -z "$env_source" ]]; then
        log_error "NSO environment not configured - env_source is empty"
        log_error "ENV_SH_PATH is: '$ENV_SH_PATH'"
        return 1
    fi
    
    # Remove existing directory if it exists
    if [[ -d "$node_dir" ]]; then
        log_debug "Removing existing directory: $node_dir"
        execute_cmd "rm -rf $node_dir"
    fi
    
    # Ensure parent directory exists
    local parent_dir=$(dirname "$node_dir")
    if [[ ! -d "$parent_dir" ]]; then
        log_debug "Creating parent directory: $parent_dir"
        execute_cmd "mkdir -p $parent_dir"
    fi
    
    # Ensure destination directory exists before ncs-setup
    # ncs-setup requires the destination directory to exist
    log_debug "Ensuring node directory exists: $node_dir"
    execute_cmd "mkdir -p $node_dir"
    
    # Create NSO runtime directory (with environment sourced)
    log_debug "Running ncs-setup for node $node_id"
    local ncs_setup_cmd="$env_source; ncs-setup --dest $node_dir --no-netsim"
    log_debug "Command: bash -c '$ncs_setup_cmd'"
    
    if [[ "${DRY_RUN}" == "true" ]]; then
        echo "[DRY-RUN] bash -c '$ncs_setup_cmd'"
    else
        if ! bash -c "$ncs_setup_cmd"; then
            log_error "Failed to run ncs-setup for node $node_id"
            log_error "Command was: bash -c '$ncs_setup_cmd'"
            return 1
        fi
    fi
    
    # Verify the directory was created
    if [[ ! -d "$node_dir" ]] && [[ "${DRY_RUN}" != "true" ]]; then
        log_error "NSO directory was not created: $node_dir"
        return 1
    fi
    
    # Apply basic configuration modifications
    apply_basic_nso_config "$node_id" "$node_dir" "$node_ip"
    
    # Apply RAFT configuration
    apply_raft_config "$node_id" "$node_dir" "$node_address" "$seed_address"
    
    # Apply SSL configuration if enabled
    if [[ "$SSL_ENABLED" == "true" ]]; then
        apply_ssl_config "$node_id" "$node_dir"
    fi
}

# Apply basic NSO configuration changes
apply_basic_nso_config() {
    local node_id="$1"
    local node_dir="$2"
    local node_ip="$3"
    local ncs_conf="$node_dir/ncs.conf"
    
    log_debug "Applying basic NSO configuration for node $node_id"
    
    # Backup original config
    execute_cmd "cp $ncs_conf ${ncs_conf}.bk"

    # Apply sed transformations for basic config  
    # Replace both 0.0.0.0 and hardcoded simple network IPs with actual node IP
    local simple_ip="${NETWORK_PREFIX}.${node_id}.1"
    execute_cmd "sed -i \\
        -e '/<webui>/,/<\\/webui>/ s|<enabled>true</enabled>|<enabled>false</enabled>|' \\
        -e 's/0\\.0\\.0\\.0/${node_ip}/g' \\
        -e 's/${simple_ip//./\\.}/${node_ip}/g' \\
        -e 's|<cli>|<cli><default-table-behavior>dynamic</default-table-behavior>|g' \\
        -e 's|</ncs-config>|<ncs-ipc-address><ip>${node_ip}</ip></ncs-ipc-address>\\n&|' \\
        \"$ncs_conf\""
}

# Apply RAFT cluster configuration
apply_raft_config() {
    local node_id="$1"
    local node_dir="$2"
    local node_address="$3"
    local seed_address="$4"
    local ncs_conf="$node_dir/ncs.conf"
    
    # Use actual 0-based node index for SSL (node_id is 1-based)
    local ssl_node_idx=$((node_id - 1))
    
    # Use correct SSL paths matching the SSL setup script output
    # Use node_id for certificate name to match the hostname (PREFIX1, PREFIX2, etc.)
    local ssl_keyfile="../erldist/ssl/ncs${ssl_node_idx}/server_key.pem"
    local ssl_certfile="../erldist/ssl/ca1/certs/${PREFIX}${node_id}_cert.pem"
    local ssl_cacertfile="../erldist/ssl/ca1/cert.pem"
    local ssl_crldir="../erldist/ssl/crl"
    
    log_debug "Applying RAFT configuration for node $node_id"
    
    # Apply RAFT configuration via sed
    execute_cmd "sed -i '/<ha-raft>/,/<\\/ha-raft>/{
        s/<!-- \\(.*\\) -->/\\1/;
        s|<\\(cluster-name\\)>[^<]*</\\1>|<\\1>${CLUSTER_NAME}</\\1>|;
        s|<\\(node-address\\)>[^<]*</\\1>|<\\1>${node_address}</\\1>|;
        s|<\\(seed-node\\)>n2\\.[^<]*</\\1>|<\\1>${seed_address}</\\1>|;
        s|<\\(key-file\\)>[^<]*</\\1>|<\\1>${ssl_keyfile}</\\1>|;
        s|<\\(cert-file\\)>[^<]*</\\1>|<\\1>${ssl_certfile}</\\1>|;
        s|<\\(ca-cert-file\\)>[^<]*</\\1>|<\\1>${ssl_cacertfile}</\\1>|;
        s|</ca-cert-file>|&<crl-dir>${ssl_crldir}</crl-dir>|;
    }' \"$ncs_conf\""
    
    # Create TCP version (SSL disabled)
    execute_cmd "sed '/<ha-raft>/,/<\\/ha-raft>/{
        /<ssl>/,/<enabled>/s/>true</>false</;
    }' \"$ncs_conf\" >\"${ncs_conf}.tcp\""
    
    # Create IP version (using IP addresses instead of hostnames)
    execute_cmd "sed '/<ha-raft>/,/<\\/ha-raft>/{
        s/@[^<]*/@127.0.0.1/g;
    }' \"$ncs_conf\" >\"${ncs_conf}.ip\""
}

# Apply SSL configuration for WebUI and Erlang distribution
apply_ssl_config() {
    local node_id="$1"
    local node_dir="$2"
    local ncs_conf="$node_dir/ncs.conf"
    
    # Use actual 0-based node index for SSL (node_id is 1-based)
    local ssl_node_idx=$((node_id - 1))
    
    # Paths based on actual SSL script output structure
    # Use node_id for certificate name to match the hostname (PREFIX1, PREFIX2, etc.)
    local ssl_keyfile="../erldist/ssl/ncs${ssl_node_idx}/server_key.pem"
    local ssl_certfile="../erldist/ssl/ca1/certs/${PREFIX}${node_id}_cert.pem"
    local ssl_cacertfile="../erldist/ssl/ca1/cert.pem"
    
    log_debug "Applying SSL configuration for node $node_id (SSL index: $ssl_node_idx)"
    
    # Configure WebUI SSL - enable and set certificate paths
    execute_cmd "sed -i '/<webui>/,/<\\/webui>/{
        s/<!-- \\(.*\\) -->/\\1/;
        s|<enabled>false</enabled>|<enabled>true</enabled>|;
        s|<ssl>.*</ssl>|<ssl><enabled>true</enabled><key-file>${ssl_keyfile}</key-file><cert-file>${ssl_certfile}</cert-file><ca-cert-file>${ssl_cacertfile}</ca-cert-file></ssl>|;
    }' \"$ncs_conf\""
    
    # Enable SSL in RAFT configuration with proper certificate paths
    execute_cmd "sed -i '/<ha-raft>/,/<\\/ha-raft>/{
        /<ssl>/,/<\\/ssl>/{
            # Delete everything between <ssl> and </ssl> tags
            /<ssl>/,/<\\/ssl>/d;
        }
        # Insert the new SSL configuration before the </ha-raft> closing tag
        /<\\/ha-raft>/i\\
    <ssl>\\
      <enabled>true</enabled>\\
      <key-file>${ssl_keyfile}</key-file>\\
      <cert-file>${ssl_certfile}</cert-file>\\
      <ca-cert-file>${ssl_cacertfile}</ca-cert-file>\\
    </ssl>
    }' \"$ncs_conf\""
    
    log_debug "SSL configuration applied - Key: $ssl_keyfile, Cert: $ssl_certfile, CA: $ssl_cacertfile"
}

