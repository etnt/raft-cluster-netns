#!/bin/bash

# RAFT Cluster Virtual Network Setup Script
# Creates isolated network namespaces for NSO RAFT cluster testing

set -euo pipefail

# Script metadata
SCRIPT_NAME="raft-cluster-netns.sh"
SCRIPT_VERSION="1.0.0"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# Default configuration
DEFAULT_NODES=3
DEFAULT_PREFIX="ha"
DEFAULT_WORK_DIR="$(pwd)"
DEFAULT_CLUSTER_NAME="test-cluster"
DEFAULT_NETWORK_PREFIX="192.168"
DEFAULT_BRIDGE_NAME="ha-cluster"
DEFAULT_SSL_ENABLED="false"
DEFAULT_TIMEOUT=30
DEFAULT_CONFIG_FILE=".raft-cluster.conf"

# Global variables
NODES="${DEFAULT_NODES}"
PREFIX="${DEFAULT_PREFIX}"
WORK_DIR="${DEFAULT_WORK_DIR}"
CLUSTER_NAME="${DEFAULT_CLUSTER_NAME}"
NETWORK_PREFIX="${DEFAULT_NETWORK_PREFIX}"
BRIDGE_NAME="${DEFAULT_BRIDGE_NAME}"
SSL_ENABLED="${DEFAULT_SSL_ENABLED}"
SSL_CERT_DIR=""
NCS_FLAGS=""
VERBOSE=false
DRY_RUN=false
FORCE=false
KEEP_CONFIG=false
TIMEOUT="${DEFAULT_TIMEOUT}"
SPECIFIC_NODE=""
NO_NSO=false
HOST="localhost.localdomain"
CONFIG_FILE=""
ENV_SH_PATH=""

# Configure command options
AUTO_GENERATE=false
CONFIG_OUTPUT=""
CONFIG_TYPE="simple"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Logging functions
log_info() {
    echo -e "${GREEN}[INFO]${NC} $*"
}

log_warn() {
    echo -e "${YELLOW}[WARN]${NC} $*" >&2
}

log_error() {
    echo -e "${RED}[ERROR]${NC} $*" >&2
}

log_debug() {
    if [[ "${VERBOSE}" == "true" ]]; then
        echo -e "${BLUE}[DEBUG]${NC} $*"
    fi
}

# Execute command with logging and dry-run support
execute_cmd() {
    local cmd="$*"
    log_debug "Executing: $cmd"
    
    if [[ "${DRY_RUN}" == "true" ]]; then
        echo "[DRY-RUN] $cmd"
        return 0
    fi
    
    if [[ "${VERBOSE}" == "true" ]]; then
        eval "$cmd"
    else
        eval "$cmd" >/dev/null 2>&1
    fi
}

# Validation functions  
validate_parameters() {
    # Validate prefix for security (no path traversal)
    if [[ "$PREFIX" =~ \.\.|/ ]]; then
        log_error "Invalid prefix: $PREFIX (cannot contain '..' or '/')"
        exit 1
    fi
    
    # Validate prefix characters (alphanumeric and basic symbols only)
    if ! [[ "$PREFIX" =~ ^[a-zA-Z0-9_-]+$ ]]; then
        log_error "Invalid prefix: $PREFIX (only alphanumeric, underscore, and hyphen allowed)"
        exit 1
    fi
    
    # Validate number of nodes if specified
    if [[ -n "$NODES" ]] && (! [[ "$NODES" =~ ^[0-9]+$ ]] || [[ "$NODES" -lt 1 ]]); then
        log_error "Invalid number of nodes: $NODES"
        exit 1
    fi
}

# Load configuration from file
load_config_file() {
    local config_file="$1"
    
    if [[ ! -f "$config_file" ]]; then
        return 0
    fi
    
    log_debug "Loading configuration from: $config_file"
    
    # Source the config file in a safe way
    while IFS='=' read -r key value; do
        # Skip comments and empty lines
        [[ $key =~ ^[[:space:]]*# ]] && continue
        [[ -z "$key" ]] && continue
        
        # Remove whitespace
        key=$(echo "$key" | xargs)
        value=$(echo "$value" | xargs)
        
        # Set variables based on config keys
        case "$key" in
            nodes) NODES="$value" ;;
            prefix) PREFIX="$value" ;;
            work_dir) WORK_DIR="$value" ;;
            cluster_name) CLUSTER_NAME="$value" ;;
            network_prefix) NETWORK_PREFIX="$value" ;;
            bridge_name) BRIDGE_NAME="$value" ;;
            ssl_enabled) SSL_ENABLED="$value" ;;
            ssl_cert_dir) SSL_CERT_DIR="$value" ;;
            ncs_flags) NCS_FLAGS="$value" ;;
            timeout) TIMEOUT="$value" ;;
            host) HOST="$value" ;;
            env_sh_path) ENV_SH_PATH="$value" ;;
        esac
    done < "$config_file"
}

# Save configuration to file
save_config_file() {
    local config_file="$1"
    
    log_debug "Saving configuration to: $config_file"
    
    cat > "$config_file" << EOF
# RAFT Cluster Configuration
# Generated by $SCRIPT_NAME v$SCRIPT_VERSION

# Cluster settings
nodes=$NODES
cluster_name=$CLUSTER_NAME
prefix=$PREFIX
work_dir=$WORK_DIR

# Network settings
network_prefix=$NETWORK_PREFIX
bridge_name=$BRIDGE_NAME

# NSO settings
ssl_enabled=$SSL_ENABLED
ssl_cert_dir=$SSL_CERT_DIR
ncs_flags=$NCS_FLAGS
host=$HOST
env_sh_path=$ENV_SH_PATH

# Timeouts
timeout=$TIMEOUT
EOF
}

# Find env.sh script
find_env_sh() {
    local search_paths=(
        "$WORK_DIR/env.sh"
        "$(pwd)/env.sh"
        "../env.sh"
        "../../env.sh"
        "../../../env.sh"
    )
    
    # If NCS_DIR is set, try to find env.sh relative to it
    if [[ -n "${NCS_DIR:-}" ]]; then
        search_paths+=("$NCS_DIR/../env.sh")
        search_paths+=("$NCS_DIR/../../env.sh")
        search_paths+=("$NCS_DIR/../../../env.sh")
    fi
    
    for path in "${search_paths[@]}"; do
        if [[ -f "$path" ]] && [[ -r "$path" ]]; then
            echo "$(cd "$(dirname "$path")" && pwd)/$(basename "$path")"
            return 0
        fi
    done
    
    return 1
}

# Prompt for env.sh path
prompt_for_env_sh() {
    echo
    log_warn "NSO environment script (env.sh) not found or not configured"
    
    # Try to find it automatically
    local found_env_sh
    if found_env_sh=$(find_env_sh); then
        log_info "Found env.sh at: $found_env_sh"
        echo -n "Use this path? [Y/n]: "
        read -r response
        if [[ -z "$response" ]] || [[ "$response" =~ ^[Yy] ]]; then
            ENV_SH_PATH="$found_env_sh"
            return 0
        fi
    fi
    
    # Manual input
    while true; do
        echo -n "Please enter the path to env.sh (or 'skip' to continue without): "
        read -r env_path
        
        if [[ "$env_path" == "skip" ]]; then
            log_warn "Continuing without env.sh - NSO commands may not work"
            return 1
        fi
        
        if [[ -f "$env_path" ]] && [[ -r "$env_path" ]]; then
            ENV_SH_PATH="$(cd "$(dirname "$env_path")" && pwd)/$(basename "$env_path")"
            break
        else
            log_error "File not found or not readable: $env_path"
        fi
    done
    
    return 0
}

# Setup NSO environment
setup_nso_environment() {
    # Check if env.sh path is configured
    if [[ -z "$ENV_SH_PATH" ]]; then
        if ! prompt_for_env_sh; then
            return 1
        fi
        
        # Save the path to config file
        if [[ -n "$CONFIG_FILE" ]]; then
            save_config_file "$CONFIG_FILE"
            log_info "Saved env.sh path to configuration file"
        fi
    fi
    
    # Verify the env.sh file exists
    if [[ -n "$ENV_SH_PATH" ]] && [[ ! -f "$ENV_SH_PATH" ]]; then
        log_error "env.sh file not found: $ENV_SH_PATH"
        return 1
    fi
    
    return 0
}

# Get NSO environment sourcing command
get_nso_env_source() {
    if [[ -n "$ENV_SH_PATH" ]]; then
        echo ". '$ENV_SH_PATH'"
    else
        echo ""
    fi
}

# Check if command exists
command_exists() {
    command -v "$1" >/dev/null 2>&1
}

# Check prerequisites
check_prerequisites() {
    log_info "Checking prerequisites..."
    
    # Check if running with sufficient privileges
    if [[ $EUID -ne 0 ]] && ! sudo -n true 2>/dev/null; then
        log_error "This script requires root privileges or passwordless sudo access"
        log_error "Add to /etc/sudoers: %sudo ALL=(ALL) NOPASSWD: /usr/sbin/ip,/usr/sbin/iptables"
        exit 1
    fi
    
    # Setup NSO environment if needed
    if [[ "$NO_NSO" != "true" ]]; then
        if ! setup_nso_environment; then
            log_error "Failed to setup NSO environment"
            exit 1
        fi
        
        # Source env.sh to check NSO commands
        local env_source=$(get_nso_env_source)
        if [[ -n "$env_source" ]]; then
            log_debug "Sourcing NSO environment: $ENV_SH_PATH"
            # Test if we can source the environment
            if ! bash -c "$env_source" >/dev/null 2>&1; then
                log_error "Failed to source NSO environment: $ENV_SH_PATH"
                exit 1
            fi
        fi
        
        # Check required NSO commands (after sourcing environment)
        local nso_commands=("ncs-setup" "ncs" "ncs_cmd")
        for cmd in "${nso_commands[@]}"; do
            if ! bash -c "$env_source; command -v $cmd" >/dev/null 2>&1; then
                log_error "Required NSO command not found: $cmd"
                if [[ -z "$env_source" ]]; then
                    log_error "Consider setting up env.sh path for NSO environment"
                fi
                exit 1
            fi
        done
    fi
    
    # Check system commands
    local required_commands=("ip" "iptables")
    for cmd in "${required_commands[@]}"; do
        if ! command_exists "$cmd"; then
            log_error "Required command not found: $cmd"
            exit 1
        fi
    done
    
    # Check NSO environment (only if env.sh is available)
    if [[ "$NO_NSO" != "true" ]] && [[ -n "$ENV_SH_PATH" ]]; then
        local env_source=$(get_nso_env_source)
        local ncs_dir=$(bash -c "$env_source; echo \${NCS_DIR:-}")
        
        if [[ -z "$ncs_dir" ]]; then
            log_error "NCS_DIR environment variable not set after sourcing $ENV_SH_PATH"
            exit 1
        fi
        
        if [[ ! -d "$ncs_dir" ]]; then
            log_error "NCS_DIR directory does not exist: $ncs_dir"
            exit 1
        fi
    fi
    
    log_info "Prerequisites check passed"
}

# Fix Docker's iptables mess
fix_dockers_mess() {
    log_info "Fixing Docker iptables policy..."
    execute_cmd "sudo iptables --policy FORWARD ACCEPT"
}

# Create virtual ethernet pairs
create_veth_pairs() {
    log_info "Creating virtual ethernet pairs..."
    
    for ((i=1; i<=NODES; i++)); do
        local veth_a="${PREFIX}${i}a"
        local veth_b="${PREFIX}${i}b"
        
        log_debug "Creating veth pair: $veth_a <-> $veth_b"
        execute_cmd "sudo ip link add dev $veth_a type veth peer name $veth_b"
    done
}

# Delete virtual ethernet pairs
delete_veth_pairs() {
    log_info "Deleting virtual ethernet pairs..."
    
    for ((i=1; i<=NODES; i++)); do
        local veth_b="${PREFIX}${i}b"
        
        if ip link show "$veth_b" >/dev/null 2>&1; then
            log_debug "Deleting veth pair: $veth_b"
            execute_cmd "sudo ip link del dev $veth_b"
        fi
    done
}

# Create hosts file for a namespace
create_hosts_file() {
    local node_id="$1"
    local hosts_dir="${WORK_DIR}/hosts/${PREFIX}${node_id}ns"
    local hosts_file="${hosts_dir}/hosts"
    
    log_debug "Creating hosts file for node $node_id"
    
    execute_cmd "mkdir -p $hosts_dir"
    
    if [[ "${DRY_RUN}" == "true" ]]; then
        log_debug "[DRY-RUN] Would create hosts file: $hosts_file"
        return 0
    fi
    
    cat > "$hosts_file" << EOF
127.0.0.1   localhost
::1         localhost ip6-localhost ip6-loopback
fe00::0     ip6-localnet
ff00::0     ip6-mcastprefix
ff02::1     ip6-allnodes
ff02::2     ip6-allrouters

# HA Cluster nodes
EOF

    # Add entries for all cluster nodes
    for ((j=1; j<=NODES; j++)); do
        echo "${NETWORK_PREFIX}.${j}.1    ${PREFIX}${j}.ha-cluster" >> "$hosts_file"
    done
}

# Create all hosts files
create_hosts_files() {
    log_info "Creating hosts files..."
    
    for ((i=1; i<=NODES; i++)); do
        create_hosts_file "$i"
    done
}

# Add hosts file to namespace
add_hosts_to_namespace() {
    local node_id="$1"
    local netns_dir="/etc/netns/${PREFIX}${node_id}ns"
    local hosts_src="${WORK_DIR}/hosts/${PREFIX}${node_id}ns/hosts"
    
    log_debug "Adding hosts file to namespace ${PREFIX}${node_id}ns"
    
    execute_cmd "sudo mkdir -p $netns_dir"
    execute_cmd "sudo cp $hosts_src $netns_dir/hosts"
}

# Remove hosts file from namespace
remove_hosts_from_namespace() {
    local node_id="$1"
    local netns_dir="/etc/netns/${PREFIX}${node_id}ns"
    
    if [[ -f "$netns_dir/hosts" ]]; then
        log_debug "Removing hosts file from namespace ${PREFIX}${node_id}ns"
        execute_cmd "sudo rm $netns_dir/hosts"
        execute_cmd "sudo rmdir $netns_dir" || true
    fi
}

# Create network namespaces
create_namespaces() {
    log_info "Creating network namespaces..."
    
    for ((i=1; i<=NODES; i++)); do
        local netns="${PREFIX}${i}ns"
        local veth_a="${PREFIX}${i}a"
        local node_ip="${NETWORK_PREFIX}.${i}.1"
        
        log_debug "Creating namespace: $netns"
        
        # Create namespace
        execute_cmd "sudo ip netns add $netns"
        
        # Move veth interface to namespace
        execute_cmd "sudo ip link set dev $veth_a netns $netns"
        
        # Configure interface in namespace
        execute_cmd "sudo ip -n $netns addr add ${node_ip}/16 dev $veth_a"
        execute_cmd "sudo ip -n $netns link set dev $veth_a up"
        execute_cmd "sudo ip -n $netns link set dev lo up"
        
        # Add default route
        execute_cmd "sudo ip -n $netns route add default via ${node_ip} dev $veth_a"
        
        # Add hosts file
        add_hosts_to_namespace "$i"
    done
}

# Delete network namespaces
delete_namespaces() {
    log_info "Deleting network namespaces..."
    
    for ((i=1; i<=NODES; i++)); do
        local netns="${PREFIX}${i}ns"
        local veth_a="${PREFIX}${i}a"
        
        if ip netns list | grep -q "^$netns"; then
            log_debug "Deleting namespace: $netns"
            
            # Move interface back to root namespace (if it exists)
            if sudo ip -n "$netns" link show "$veth_a" >/dev/null 2>&1; then
                execute_cmd "sudo ip -n $netns link set dev $veth_a netns 1" || true
            fi
            
            # Delete namespace
            execute_cmd "sudo ip netns del $netns"
            
            # Remove hosts file
            remove_hosts_from_namespace "$i"
        fi
    done
}

# Setup bridge network
setup_bridge() {
    log_info "Setting up bridge network..."
    
    # Create bridge
    execute_cmd "sudo ip link add dev $BRIDGE_NAME type bridge"
    
    # Connect all veth interfaces to bridge
    for ((i=1; i<=NODES; i++)); do
        local veth_b="${PREFIX}${i}b"
        
        log_debug "Connecting $veth_b to bridge $BRIDGE_NAME"
        execute_cmd "sudo ip link set dev $veth_b master $BRIDGE_NAME"
        execute_cmd "sudo ip link set dev $veth_b up"
    done
    
    # Add gateway address to bridge
    execute_cmd "sudo ip addr add ${NETWORK_PREFIX}.0.254/16 dev $BRIDGE_NAME"
    execute_cmd "sudo ip link set dev $BRIDGE_NAME up"
}

# Cleanup bridge network
cleanup_bridge() {
    log_info "Cleaning up bridge network..."
    
    if ip link show "$BRIDGE_NAME" >/dev/null 2>&1; then
        # Disconnect interfaces from bridge
        for ((i=1; i<=NODES; i++)); do
            local veth_b="${PREFIX}${i}b"
            
            if ip link show "$veth_b" >/dev/null 2>&1; then
                log_debug "Disconnecting $veth_b from bridge"
                execute_cmd "sudo ip link set $veth_b nomaster" || true
                execute_cmd "sudo ip link set $veth_b down" || true
            fi
        done
        
        # Delete bridge
        execute_cmd "sudo ip link set dev $BRIDGE_NAME down"
        execute_cmd "sudo ip link del $BRIDGE_NAME"
    fi
}

# Validate network connectivity
validate_network() {
    log_info "Validating network connectivity..."
    
    if [[ "${DRY_RUN}" == "true" ]]; then
        log_debug "[DRY-RUN] Skipping network validation"
        return 0
    fi
    
    # Check bridge interface
    if ! ip addr show "$BRIDGE_NAME" | grep -q "UP,LOWER_UP"; then
        log_error "Bridge interface $BRIDGE_NAME is not up"
        return 1
    fi
    
    if ! ip addr show "$BRIDGE_NAME" | grep -q "inet ${NETWORK_PREFIX}.0.254/16"; then
        log_error "Bridge interface missing gateway address"
        return 1
    fi
    
    # Check namespace interfaces
    for ((i=1; i<=NODES; i++)); do
        local netns="${PREFIX}${i}ns"
        local veth_a="${PREFIX}${i}a"
        local veth_b="${PREFIX}${i}b"
        local node_ip="${NETWORK_PREFIX}.${i}.1"
        
        # Check veth pair is up
        if ! ip link show "$veth_b" | grep -q "UP,LOWER_UP"; then
            log_error "Interface $veth_b is not up"
            return 1
        fi
        
        # Check namespace interface
        if ! sudo ip -n "$netns" addr show "$veth_a" | grep -q "UP,LOWER_UP"; then
            log_error "Namespace interface $veth_a in $netns is not up"
            return 1
        fi
        
        if ! sudo ip -n "$netns" addr show "$veth_a" | grep -q "inet ${node_ip}/16"; then
            log_error "Namespace interface missing IP address: $node_ip"
            return 1
        fi
    done
    
    log_info "Network validation passed"
}

# Test network connectivity between nodes
test_connectivity() {
    log_info "Testing network connectivity..."
    
    if [[ "${DRY_RUN}" == "true" ]]; then
        log_debug "[DRY-RUN] Skipping connectivity tests"
        return 0
    fi
    
    for ((i=1; i<=NODES; i++)); do
        local netns="${PREFIX}${i}ns"
        
        # Test ping to next node (circular)
        local next_node=$((i % NODES + 1))
        local target_ip="${NETWORK_PREFIX}.${next_node}.1"
        
        log_debug "Testing ping from node $i to node $next_node"
        
        if ! execute_cmd "sudo ip netns exec $netns ping -c 1 -W 2 $target_ip"; then
            log_error "Ping failed from node $i to node $next_node"
            return 1
        fi
        
        # Test hostname resolution
        local target_hostname="${PREFIX}${next_node}.ha-cluster"
        if ! execute_cmd "sudo ip netns exec $netns ping -c 1 -W 2 $target_hostname"; then
            log_error "Hostname resolution failed from node $i to $target_hostname"
            return 1
        fi
    done
    
    log_info "Connectivity test passed"
}

# Isolate a single node from the cluster (simulate cable disconnect)
isolate_node() {
    local node_id="$1"
    local netns="${PREFIX}${node_id}ns"
    local veth_b="${PREFIX}${node_id}b"
    
    if ! ip netns list | grep -q "^$netns"; then
        log_error "Namespace $netns does not exist"
        return 1
    fi
    
    if ! ip link show "$veth_b" >/dev/null 2>&1; then
        log_error "Interface $veth_b does not exist"
        return 1
    fi
    
    log_info "Isolating node $node_id from cluster (simulating cable disconnect)"
    
    # Method 1: Bring down the bridge-side interface
    log_debug "Bringing down interface $veth_b"
    execute_cmd "sudo ip link set $veth_b down"
    
    # Method 2: Remove from bridge (but keep interface up for later healing)
    log_debug "Removing $veth_b from bridge $BRIDGE_NAME"
    execute_cmd "sudo ip link set $veth_b nomaster"
    
    log_info "Node $node_id is now isolated from the cluster"
    log_info "Use 'heal $node_id' or 'heal' to restore connectivity"
}

# Create a network partition between groups of nodes
create_partition() {
    local partition_spec="$1"
    
    if [[ ! "$partition_spec" =~ ^[0-9,]+$ ]]; then
        log_error "Invalid partition specification. Use format like '1,2' to isolate nodes 1 and 2"
        return 1
    fi
    
    # Parse the partition specification
    IFS=',' read -ra partition_nodes <<< "$partition_spec"
    
    # Validate node IDs
    for node_id in "${partition_nodes[@]}"; do
        if [[ ! "$node_id" =~ ^[0-9]+$ ]] || [[ "$node_id" -lt 1 ]] || [[ "$node_id" -gt "$NODES" ]]; then
            log_error "Invalid node ID: $node_id (must be between 1 and $NODES)"
            return 1
        fi
    done
    
    log_info "Creating network partition: nodes [${partition_spec}] vs remaining nodes"
    
    # Create a temporary bridge for the partitioned nodes
    local partition_bridge="${BRIDGE_NAME}part"
    log_debug "Creating partition bridge: $partition_bridge"
    execute_cmd "sudo ip link add dev $partition_bridge type bridge"
    execute_cmd "sudo ip addr add ${NETWORK_PREFIX}.99.254/16 dev $partition_bridge"
    execute_cmd "sudo ip link set dev $partition_bridge up"
    
    # Move specified nodes to the partition bridge
    for node_id in "${partition_nodes[@]}"; do
        local veth_b="${PREFIX}${node_id}b"
        local netns="${PREFIX}${node_id}ns"
        
        if ! ip netns list | grep -q "^$netns"; then
            log_warn "Namespace $netns does not exist, skipping node $node_id"
            continue
        fi
        
        if ! ip link show "$veth_b" >/dev/null 2>&1; then
            log_warn "Interface $veth_b does not exist, skipping node $node_id"
            continue
        fi
        
        log_debug "Moving node $node_id to partition bridge"
        execute_cmd "sudo ip link set $veth_b nomaster"
        execute_cmd "sudo ip link set $veth_b master $partition_bridge"
        execute_cmd "sudo ip link set $veth_b up"
    done
    
    log_info "Network partition created successfully"
    log_info "Partitioned nodes: [${partition_spec}]"
    log_info "Main cluster nodes: [$(get_remaining_nodes "$partition_spec")]"
    log_info "Use 'heal' to restore full connectivity"
}

# Get list of nodes not in the partition
get_remaining_nodes() {
    local partition_spec="$1"
    IFS=',' read -ra partition_nodes <<< "$partition_spec"
    
    local remaining=()
    for ((i=1; i<=NODES; i++)); do
        local is_partitioned=false
        for partitioned_node in "${partition_nodes[@]}"; do
            if [[ "$i" == "$partitioned_node" ]]; then
                is_partitioned=true
                break
            fi
        done
        if [[ "$is_partitioned" == "false" ]]; then
            remaining+=("$i")
        fi
    done
    
    # Join array with commas
    local IFS=','
    echo "${remaining[*]}"
}

# Heal network partition (restore connectivity)
heal_partition() {
    local specific_node="$1"
    
    if [[ -n "$specific_node" ]]; then
        log_info "Healing connectivity for node $specific_node"
        heal_single_node "$specific_node"
    else
        log_info "Healing all network partitions"
        heal_all_nodes
    fi
}

# Heal a specific node
heal_single_node() {
    local node_id="$1"
    local netns="${PREFIX}${node_id}ns"
    local veth_b="${PREFIX}${node_id}b"
    
    if ! ip netns list | grep -q "^$netns"; then
        log_error "Namespace $netns does not exist"
        return 1
    fi
    
    if ! ip link show "$veth_b" >/dev/null 2>&1; then
        log_error "Interface $veth_b does not exist"
        return 1
    fi
    
    log_debug "Restoring connectivity for node $node_id"
    
    # Remove from any partition bridge
    execute_cmd "sudo ip link set $veth_b nomaster" 2>/dev/null || true
    
    # Add back to main bridge
    execute_cmd "sudo ip link set $veth_b master $BRIDGE_NAME"
    execute_cmd "sudo ip link set $veth_b up"
    
    log_info "Node $node_id connectivity restored"
}

# Heal all nodes and remove partition bridges
heal_all_nodes() {
    # First, restore all nodes to main bridge
    for ((i=1; i<=NODES; i++)); do
        local veth_b="${PREFIX}${i}b"
        if ip link show "$veth_b" >/dev/null 2>&1; then
            log_debug "Restoring node $i to main bridge"
            execute_cmd "sudo ip link set $veth_b nomaster" 2>/dev/null || true
            execute_cmd "sudo ip link set $veth_b master $BRIDGE_NAME"
            execute_cmd "sudo ip link set $veth_b up"
        fi
    done
    
    # Remove any partition bridges
    local partition_bridge="${BRIDGE_NAME}part"
    if ip link show "$partition_bridge" >/dev/null 2>&1; then
        log_debug "Removing partition bridge: $partition_bridge"
        execute_cmd "sudo ip link set dev $partition_bridge down"
        execute_cmd "sudo ip link del $partition_bridge"
    fi
    
    log_info "All network partitions healed - full connectivity restored"
}

# Test connectivity and show partition status
show_partition_status() {
    log_info "Network Partition Status:"
    echo ""
    
    # Show bridge status
    echo "  Bridges:"
    if ip link show "$BRIDGE_NAME" >/dev/null 2>&1; then
        local main_members=$(bridge link show | grep "$BRIDGE_NAME" | wc -l)
        echo "    Main bridge ($BRIDGE_NAME): $main_members members"
        bridge link show | grep "$BRIDGE_NAME" | while read -r line; do
            echo "      $line"
        done
    fi
    
    local partition_bridge="${BRIDGE_NAME}part"
    if ip link show "$partition_bridge" >/dev/null 2>&1; then
        local partition_members=$(bridge link show | grep "$partition_bridge" | wc -l)
        echo "    Partition bridge ($partition_bridge): $partition_members members"
        bridge link show | grep "$partition_bridge" | while read -r line; do
            echo "      $line"
        done
    fi
    
    echo ""
    echo "  Node Connectivity Matrix:"
    echo -n "    From\\To: "
    for ((j=1; j<=NODES; j++)); do
        printf "%8s" "Node$j"
    done
    echo ""
    
    # Test connectivity between all pairs
    for ((i=1; i<=NODES; i++)); do
        local netns_i="${PREFIX}${i}ns"
        printf "%12s" "Node$i:"
        
        for ((j=1; j<=NODES; j++)); do
            if [[ "$i" == "$j" ]]; then
                printf "%8s" "SELF"
            else
                local target_ip="${NETWORK_PREFIX}.${j}.1"
                if sudo ip netns exec "$netns_i" ping -c 1 -W 1 "$target_ip" >/dev/null 2>&1; then
                    printf "%8s" "✓"
                else
                    printf "%8s" "✗"
                fi
            fi
        done
        echo ""
    done
}

# Get seed node address for a given node
get_seed_node_address() {
    local node_id="$1"
    local seed_id=$((node_id % NODES + 1))
    echo "ncsd${seed_id}@${PREFIX}${seed_id}.ha-cluster"
}

# Setup NSO node directory and configuration
setup_nso_node() {
    local node_id="$1"
    local node_dir="${WORK_DIR}/ncs-run${node_id}"
    local node_ip="${NETWORK_PREFIX}.${node_id}.1"
    local node_address="ncsd${node_id}@${PREFIX}${node_id}.ha-cluster"
    local seed_address=$(get_seed_node_address "$node_id")
    local env_source=$(get_nso_env_source)
    
    log_debug "Setting up NSO node $node_id in $node_dir"
    
    # Remove existing directory if it exists
    if [[ -d "$node_dir" ]]; then
        log_debug "Removing existing directory: $node_dir"
        execute_cmd "rm -rf $node_dir"
    fi
    
    # Ensure parent directory exists
    local parent_dir=$(dirname "$node_dir")
    if [[ ! -d "$parent_dir" ]]; then
        log_debug "Creating parent directory: $parent_dir"
        execute_cmd "mkdir -p $parent_dir"
    fi
    
    # Create NSO runtime directory (with environment sourced)
    log_debug "Running ncs-setup for node $node_id"
    if [[ "${DRY_RUN}" == "true" ]]; then
        echo "[DRY-RUN] bash -c '$env_source; ncs-setup --dest $node_dir --no-netsim'"
    else
        if ! bash -c "$env_source; ncs-setup --dest $node_dir --no-netsim"; then
            log_error "Failed to run ncs-setup for node $node_id"
            return 1
        fi
    fi
    
    # Verify the directory was created
    if [[ ! -d "$node_dir" ]] && [[ "${DRY_RUN}" != "true" ]]; then
        log_error "NSO directory was not created: $node_dir"
        return 1
    fi
    
    # Apply basic configuration modifications
    apply_basic_nso_config "$node_id" "$node_dir" "$node_ip"
    
    # Apply RAFT configuration
    apply_raft_config "$node_id" "$node_dir" "$node_address" "$seed_address"
    
    # Apply SSL configuration if enabled
    if [[ "$SSL_ENABLED" == "true" ]]; then
        apply_ssl_config "$node_id" "$node_dir"
    fi
}

# Apply basic NSO configuration changes
apply_basic_nso_config() {
    local node_id="$1"
    local node_dir="$2"
    local node_ip="$3"
    local ncs_conf="$node_dir/ncs.conf"
    
    log_debug "Applying basic NSO configuration for node $node_id"
    
    # Backup original config
    execute_cmd "cp $ncs_conf ${ncs_conf}.bk"
    
    # Apply sed transformations for basic config  
    execute_cmd "sed -i \\
        -e '/<webui>/,/<\\/webui>/ s|<enabled>true</enabled>|<enabled>false</enabled>|' \\
        -e 's/0\\.0\\.0\\.0/${node_ip}/g' \\
        -e 's|<cli>|<cli><default-table-behavior>dynamic</default-table-behavior>|g' \\
        -e 's|</ncs-config>|<ncs-ipc-address><ip>${node_ip}</ip></ncs-ipc-address>\\n&|' \\
        \"$ncs_conf\""
}

# Apply RAFT cluster configuration
apply_raft_config() {
    local node_id="$1"
    local node_dir="$2"
    local node_address="$3"
    local seed_address="$4"
    local ncs_conf="$node_dir/ncs.conf"
    local ssl_keyfile="../erldist/ssl/ncs${node_id}/server_key.pem"
    local ssl_certfile="../erldist/ssl/ca/certs/${PREFIX}${node_id}_cert.pem"
    local ssl_cacertfile="../erldist/ssl/ca/cert.pem"
    local ssl_crldir="../erldist/ssl/crl"
    
    log_debug "Applying RAFT configuration for node $node_id"
    
    # Apply RAFT configuration via sed
    execute_cmd "sed -i '/<ha-raft>/,/<\\/ha-raft>/{
        s/<!-- \\(.*\\) -->/\\1/;
        s|<\\(cluster-name\\)>[^<]*</\\1>|<\\1>${CLUSTER_NAME}</\\1>|;
        s|<\\(node-address\\)>[^<]*</\\1>|<\\1>${node_address}</\\1>|;
        s|<\\(seed-node\\)>n2\\.[^<]*</\\1>|<\\1>${seed_address}</\\1>|;
        s|<\\(key-file\\)>[^<]*</\\1>|<\\1>${ssl_keyfile}</\\1>|;
        s|<\\(cert-file\\)>[^<]*</\\1>|<\\1>${ssl_certfile}</\\1>|;
        s|<\\(ca-cert-file\\)>[^<]*</\\1>|<\\1>${ssl_cacertfile}</\\1>|;
        s|</ca-cert-file>|&<crl-dir>${ssl_crldir}</crl-dir>|;
    }' \"$ncs_conf\""
    
    # Create TCP version (SSL disabled)
    execute_cmd "sed '/<ha-raft>/,/<\\/ha-raft>/{
        /<ssl>/,/<enabled>/s/>true</>false</;
    }' \"$ncs_conf\" >\"${ncs_conf}.tcp\""
    
    # Create IP version (using IP addresses instead of hostnames)
    execute_cmd "sed '/<ha-raft>/,/<\\/ha-raft>/{
        s/@[^<]*/@127.0.0.1/g;
    }' \"$ncs_conf\" >\"${ncs_conf}.ip\""
}

# Apply SSL configuration for WebUI and Erlang distribution
apply_ssl_config() {
    local node_id="$1"
    local node_dir="$2"
    local ncs_conf="$node_dir/ncs.conf"
    local ssl_keyfile="../erldist/ssl/ncs${node_id}/server_key.pem"
    local ssl_certfile="../erldist/ssl/ca/certs/${PREFIX}${node_id}_cert.pem"
    local ssl_cacertfile="../erldist/ssl/ca/cert.pem"
    
    log_debug "Applying SSL configuration for node $node_id"
    
    # Configure WebUI SSL
    execute_cmd "sed -i '/<webui>/,/<\\/webui>/{
        s/<!-- \\(.*\\) -->/\\1/;
        s|<\\(key-file\\)>[^<]*</\\1>|<\\1>${ssl_keyfile}</\\1>|;
        s|<\\(cert-file\\)>[^<]*</\\1>|<\\1>${ssl_certfile}</\\1>|;
        s|</cert-file>|&<ca-cert-file>${ssl_cacertfile}</ca-cert-file>|;
    }' \"$ncs_conf\""
    
    # Enable SSL in RAFT configuration
    execute_cmd "sed -i '/<ha-raft>/,/<\\/ha-raft>/{
        s|<ssl>.*|<ssl><enabled>true</enabled>|;
    }' \"$ncs_conf\""
}

