# Cleanup complete environment (NSO + network)
cleanup_environment() {
    local cleanup_failed=false
    
    if [[ "$NO_NSO" != "true" ]]; then
        if ! stop_nso_nodes; then
            log_warn "Failed to stop some NSO nodes, continuing with cleanup..."
            cleanup_failed=true
        fi
        
        if ! cleanup_nso_nodes; then
            log_warn "Failed to cleanup some NSO directories, continuing with network cleanup..."
            cleanup_failed=true
        fi
    fi
    
    if ! cleanup_network; then
        log_error "Failed to cleanup network infrastructure"
        cleanup_failed=true
    fi
    
    # Cleanup SSL certificates
    if ! cleanup_ssl_certificates; then
        log_warn "Failed to cleanup SSL certificates, continuing..."
        cleanup_failed=true
    fi
    
    if [[ "$cleanup_failed" == "true" ]]; then
        log_warn "Cleanup completed with some errors"
    else
        log_info "Environment cleanup completed successfully"
    fi
}

