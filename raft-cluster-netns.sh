# Parse command line arguments
parse_args() {
    if [[ $# -eq 0 ]]; then
        show_usage
        exit 1
    fi
    
    local command="$1"
    shift
    
    # First pass: look for config file option
    local temp_args=("$@")
    for ((i=0; i<${#temp_args[@]}; i++)); do
        if [[ "${temp_args[i]}" == "-c" ]] || [[ "${temp_args[i]}" == "--config" ]]; then
            CONFIG_FILE="${temp_args[i+1]}"
            break
        fi
    done
    
    # Set default config file path if not specified
    if [[ -z "$CONFIG_FILE" ]]; then
        CONFIG_FILE="$DEFAULT_CONFIG_FILE"
    fi
    
    # Load configuration file if it exists
    if [[ -f "$CONFIG_FILE" ]]; then
        load_config_file "$CONFIG_FILE"
    fi
    
    # Second pass: parse all arguments (these will override config file)
    while [[ $# -gt 0 ]]; do
        case $1 in
            -n|--nodes)
                NODES="$2"
                shift 2
                ;;
            -p|--prefix)
                PREFIX="$2"
                shift 2
                ;;
            -d|--work-dir)
                WORK_DIR="$2"
                shift 2
                ;;
            -c|--config)
                # Already handled in first pass
                shift 2
                ;;
            --cluster-name)
                CLUSTER_NAME="$2"
                shift 2
                ;;
            --network-prefix)
                NETWORK_PREFIX="$2"
                shift 2
                ;;
            --bridge-name)
                BRIDGE_NAME="$2"
                shift 2
                ;;
            --no-ssl)
                SSL_ENABLED=false
                shift
                ;;
            --ssl-cert-dir)
                SSL_CERT_DIR="$2"
                shift 2
                ;;
            --ncs-flags)
                NCS_FLAGS="$2"
                shift 2
                ;;
            --no-nso)
                NO_NSO=true
                shift
                ;;
            --timeout)
                TIMEOUT="$2"
                shift 2
                ;;
            --keep-config)
                KEEP_CONFIG=true
                shift
                ;;
            -v|--verbose)
                VERBOSE=true
                shift
                ;;
            --dry-run)
                DRY_RUN=true
                shift
                ;;
            --force)
                FORCE=true
                shift
                ;;
            --auto)
                AUTO_GENERATE=true
                shift
                ;;
            --output)
                shift
                CONFIG_OUTPUT="$1"
                shift
                ;;
            --type)
                shift
                CONFIG_TYPE="$1"
                shift
                ;;
            -h|--help)
                show_usage
                exit 0
                ;;
            *)
                if [[ "$command" == "shell" ]] || [[ "$command" == "exec" ]]; then
                    SPECIFIC_NODE="$1"
                    shift
                    if [[ "$command" == "exec" ]] && [[ $# -gt 0 ]]; then
                        # Collect command arguments until we hit a global option
                        local cmd_args=()
                        while [[ $# -gt 0 ]]; do
                            case "$1" in
                                -c|--config|-v|--verbose|-h|--help|--dry-run)
                                    # This is a global option, stop collecting command args
                                    break
                                    ;;
                                *)
                                    # This is part of the command to execute
                                    cmd_args+=("$1")
                                    shift
                                    ;;
                            esac
                        done
                        EXEC_COMMAND="${cmd_args[*]}"
                    fi
                elif [[ "$command" == "start" ]] || [[ "$command" == "stop" ]] || [[ "$command" == "isolate" ]] || [[ "$command" == "heal" ]]; then
                    # Optional node ID for these commands
                    if [[ "$1" =~ ^[0-9,]+$ ]]; then
                        SPECIFIC_NODE="$1"
                        shift
                    else
                        log_error "Unknown option: $1"
                        exit 1
                    fi
                elif [[ "$command" == "partition" ]]; then
                    # Partition specification required
                    if [[ "$1" =~ ^[0-9,]+$ ]]; then
                        SPECIFIC_NODE="$1"
                        shift
                    else
                        log_error "Invalid partition specification: $1"
                        exit 1
                    fi
                else
                    log_error "Unknown option: $1"
                    exit 1
                fi
                ;;
        esac
    done
    
    # Validate arguments
    if ! [[ "$NODES" =~ ^[0-9]+$ ]] || [[ "$NODES" -lt 1 ]]; then
        log_error "Invalid number of nodes: $NODES"
        exit 1
    fi
    
    # Validate prefix for security (no path traversal)
    if [[ "$PREFIX" =~ \.\.|/ ]]; then
        log_error "Invalid prefix: $PREFIX (cannot contain '..' or '/')"
        exit 1
    fi
    
    # Validate prefix characters (alphanumeric and basic symbols only)
    if ! [[ "$PREFIX" =~ ^[a-zA-Z0-9_-]+$ ]]; then
        log_error "Invalid prefix: $PREFIX (only alphanumeric, underscore, and hyphen allowed)"
        exit 1
    fi
    
    # Create work directory if it doesn't exist
    if [[ ! -d "$WORK_DIR" ]]; then
        mkdir -p "$WORK_DIR"
    fi
    
    WORK_DIR="$(cd "$WORK_DIR" && pwd)"  # Convert to absolute path
    
    # Execute command
    case "$command" in
        setup)
            setup_environment
            ;;
        start)
            # Parse L3BGP config if needed for proper IP addressing
            if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
                source "$SCRIPT_DIR/lib/network-l3bgp.sh"
                parse_l3bgp_config
            fi
            start_nso_nodes
            ;;
        stop)
            # Parse L3BGP config if needed for proper IP addressing
            if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
                source "$SCRIPT_DIR/lib/network-l3bgp.sh"
                parse_l3bgp_config
            fi
            stop_nso_nodes
            ;;
        cleanup)
            # Parse L3BGP config if needed for proper IP addressing
            if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
                source "$SCRIPT_DIR/lib/network-l3bgp.sh"
                parse_l3bgp_config
            fi
            cleanup_environment
            ;;
        status)
            # Parse L3BGP config if needed for proper status display
            if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
                source "$SCRIPT_DIR/lib/network-l3bgp.sh"
                parse_l3bgp_config
            fi
            show_status
            ;;
        shell)
            if [[ -z "$SPECIFIC_NODE" ]]; then
                log_error "Node ID required for shell command"
                exit 1
            fi
            # Parse L3BGP config if needed for proper IP addressing
            if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
                source "$SCRIPT_DIR/lib/network-l3bgp.sh"
                parse_l3bgp_config
            fi
            enter_namespace_shell "$SPECIFIC_NODE"
            ;;
        exec)
            if [[ -z "$SPECIFIC_NODE" ]] || [[ -z "${EXEC_COMMAND:-}" ]]; then
                log_error "Node ID and command required for exec"
                exit 1
            fi
            # Parse L3BGP config if needed for proper IP addressing
            if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
                source "$SCRIPT_DIR/lib/network-l3bgp.sh"
                parse_l3bgp_config
            fi
            exec_in_namespace "$SPECIFIC_NODE" "$EXEC_COMMAND"
            ;;
        test)
            test_connectivity
            ;;
        isolate)
            if [[ -z "$SPECIFIC_NODE" ]]; then
                log_error "Node ID required for isolate command"
                exit 1
            fi
            isolate_node "$SPECIFIC_NODE"
            ;;
        partition)
            if [[ -z "$SPECIFIC_NODE" ]]; then
                log_error "Partition specification required (e.g., '1,2')"
                exit 1
            fi
            create_partition "$SPECIFIC_NODE"
            ;;
        heal)
            heal_partition "$SPECIFIC_NODE"
            ;;
        configure)
            run_configure_wizard
            ;;
        help)
            show_usage
            ;;
        *)
            log_error "Unknown command: $command"
            show_usage
            exit 1
            ;;
    esac
}

# Show current status
show_status() {
    log_info "Cluster Status:"
    echo "  Nodes: $NODES"
    echo "  Prefix: $PREFIX"
    echo "  Work Directory: $WORK_DIR"
    echo "  Cluster Name: $CLUSTER_NAME"
    echo "  Network Prefix: $NETWORK_PREFIX"
    echo "  Bridge Name: $BRIDGE_NAME"
    echo "  SSL Enabled: $SSL_ENABLED"
    echo ""
    
    # Check bridge status
    if ip link show "$BRIDGE_NAME" >/dev/null 2>&1; then
        echo -e "  Bridge: ${GREEN}UP${NC}"
        ip addr show "$BRIDGE_NAME" | grep -E "inet|UP"
    else
        echo -e "  Bridge: ${RED}DOWN${NC}"
    fi
    
    echo ""
    echo "  Namespaces:"
    for ((i=1; i<=NODES; i++)); do
        local netns="${PREFIX}${i}ns"
        if ip netns list | grep -q "^$netns"; then
            echo -e "    $netns: ${GREEN}EXISTS${NC}"
        else
            echo -e "    $netns: ${RED}MISSING${NC}"
        fi
    done
    
    # Show manager namespace for L3BGP
    if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
        local mgr_ns="${PREFIX}manager"
        if ip netns list | grep -q "^$mgr_ns"; then
            echo -e "    $mgr_ns: ${GREEN}EXISTS${NC}"
        else
            echo -e "    $mgr_ns: ${RED}MISSING${NC}"
        fi
    fi
    
    echo ""
    show_nso_status
    
    echo ""
    show_partition_status
}

# Enter namespace shell
enter_namespace_shell() {
    local node_id="$1"
    local netns="${PREFIX}${node_id}ns"
    local env_source=$(get_nso_env_source)
    
    # Calculate node IP based on network type
    local node_ip
    if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
        node_ip=$(eval echo "\$NODE_${node_id}_IP")
        if [[ -z "$node_ip" ]]; then
            node_ip="${NETWORK_PREFIX}.${node_id}.1"  # fallback
        fi
    else
        node_ip="${NETWORK_PREFIX}.${node_id}.1"
    fi

    if ! ip netns list | grep -q "^$netns"; then
        log_error "Namespace $netns does not exist"
        exit 1
    fi

    log_info "Entering shell for namespace $netns"
    log_info "Use 'exit' to return to host shell"

    local user_name=$(id -un)
    local user_home=$(getent passwd "$user_name" | cut -d: -f6)
    local start_dir="${WORK_DIR}/ncs-run${node_id}"

    # Create a temporary init file for the shell
    local init_file=$(mktemp)
    cat > "$init_file" << EOF
# Source NSO environment
$env_source
cd '$start_dir'
export PS1='[$netns] \u@\h:\w$ '
export NCS_IPC_ADDR=$node_ip
echo 'Namespace: $netns'
echo 'Node IP: $node_ip'
echo 'NSO Environment: ${ENV_SH_PATH:-not configured}'
echo 'Available hosts:'
cat /etc/hosts | grep ha-cluster 2>/dev/null || echo "  (no ha-cluster hosts found)"
echo 'NSO Commands available: ncs, ncs_cli, ncs_cmd'
EOF
    
    # Enter the namespace with the init file
    sudo ip netns exec "$netns" sudo -u "$user_name" bash --init-file "$init_file"
    
    # Clean up the temporary init file
    rm -f "$init_file"
}

# Execute command in namespace
exec_in_namespace() {
    local node_id="$1"
    local cmd="$2"
    local netns="${PREFIX}${node_id}ns"
    local env_source=$(get_nso_env_source)
    
    # Calculate node IP based on network type
    local node_ip
    if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
        node_ip=$(eval echo "\$NODE_${node_id}_IP")
        if [[ -z "$node_ip" ]]; then
            node_ip="${NETWORK_PREFIX}.${node_id}.1"  # fallback
        fi
    else
        node_ip="${NETWORK_PREFIX}.${node_id}.1"
    fi

    if ! ip netns list | grep -q "^$netns"; then
        log_error "Namespace $netns does not exist"
        exit 1
    fi

    log_debug "Executing in $netns: $cmd"

    local user_name=$(id -un)
    local start_dir="$PWD"
    sudo ip netns exec "$netns" sudo -u "$user_name" bash -c "
        $env_source
        cd '$start_dir'
        export NCS_IPC_ADDR=$node_ip
        $cmd
    "
}

