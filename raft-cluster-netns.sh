# Load configuration from file
load_config_file() {
    local config_file="$1"
    
    if [[ ! -f "$config_file" ]]; then
        return 0
    fi
    
    log_debug "Loading configuration from: $config_file"
    
    # Source the config file in a safe way
    while IFS='=' read -r key value; do
        # Skip comments and empty lines
        [[ $key =~ ^[[:space:]]*# ]] && continue
        [[ -z "$key" ]] && continue
        
        # Remove whitespace
        key=$(echo "$key" | xargs)
        value=$(echo "$value" | xargs)
        
        # Set variables based on config keys
        case "$key" in
            nodes) NODES="$value" ;;
            prefix) PREFIX="$value" ;;
            work_dir) WORK_DIR="$value" ;;
            cluster_name) CLUSTER_NAME="$value" ;;
            network_prefix) NETWORK_PREFIX="$value" ;;
            network_type) NETWORK_TYPE="$value" ;;
            bridge_name) BRIDGE_NAME="$value" ;;
            ssl_enabled) SSL_ENABLED="$value" ;;
            ssl_cert_dir) SSL_CERT_DIR="$value" ;;
            ncs_flags) NCS_FLAGS="$value" ;;
            timeout) TIMEOUT="$value" ;;
            host) HOST="$value" ;;
            env_sh_path) ENV_SH_PATH="$value" ;;
            manager_enabled) MANAGER_ENABLED="$value" ;;
            manager_ip) MANAGER_IP="$value" ;;
            node_*) 
                # Handle node-specific configuration (e.g., node_1_ip, node_1_hostname)
                log_debug "Setting node variable: $key=$value"
                declare -g "$key=$value"
                ;;
        esac
    done < "$config_file"
}

