# Trap handler for cleanup on exit
cleanup_on_exit() {
    local exit_code=$?
    # Only show warning for actual failures (not normal exit or user interruption)
    if [[ $exit_code -ne 0 ]] && [[ $exit_code -ne 130 ]] && [[ "${DRY_RUN}" != "true" ]]; then
        log_warn "Script failed, you may need to run 'cleanup' manually"
    fi
    exit $exit_code
}

# Set trap for cleanup
trap cleanup_on_exit INT TERM

