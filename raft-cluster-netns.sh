# Start a single NSO node
start_nso_node() {
    local node_id="$1"
    local node_dir="${WORK_DIR}/ncs-run${node_id}"
    local netns="${PREFIX}${node_id}ns"
    local node_ip
    
    # Use L3BGP node IP if configured, otherwise fall back to simple addressing
    if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
        node_ip=$(eval echo "\$NODE_${node_id}_IP")
        if [[ -z "$node_ip" ]]; then
            log_error "L3BGP node IP not found for node $node_id. Ensure L3BGP config is parsed."
            return 1
        fi
    else
        node_ip="${NETWORK_PREFIX}.${node_id}.1"
    fi
    
    local env_source=$(get_nso_env_source)

    if [[ ! -d "$node_dir" ]]; then
        log_error "NSO node directory does not exist: $node_dir"
        return 1
    fi

    log_debug "Starting NSO node $node_id in namespace $netns (IP: $node_ip)"

    # Start NCS in the namespace (with environment sourced, as local user)
    local user_name=$(id -un)
    execute_cmd "sudo ip netns exec $netns sudo -u $user_name bash -c '
        $env_source
        export NCS_IPC_ADDR=$node_ip
        export NCS_DEBUG_DUMP_NAME=\"\${LUX_EXTRA_LOGS:-/tmp}/$node_dir/stop.dump\"
        cd $node_dir
        ncs $NCS_FLAGS
    '"
}

# Start NSO nodes
start_nso_nodes() {
    if [[ "$NO_NSO" == "true" ]]; then
        log_info "Skipping NSO start (--no-nso flag set)"
        return
    fi
    
    log_info "Starting NSO nodes..."
    
    if [[ -n "$SPECIFIC_NODE" ]]; then
        # Start specific node
        start_nso_node "$SPECIFIC_NODE"
        log_info "Started NSO node $SPECIFIC_NODE"
    else
        # Start all nodes
        for ((i=1; i<=NODES; i++)); do
            start_nso_node "$i"
        done
        log_info "All NSO nodes started"
    fi
}

# Stop a single NSO node
stop_nso_node() {
    local node_id="$1"
    local node_ip
    
    # Use L3BGP node IP if configured, otherwise fall back to simple addressing  
    if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
        node_ip=$(eval echo "\$NODE_${node_id}_IP")
        if [[ -z "$node_ip" ]]; then
            node_ip="${NETWORK_PREFIX}.${node_id}.1"  # fallback
        fi
    else
        node_ip="${NETWORK_PREFIX}.${node_id}.1"
    fi
    
    local netns="${PREFIX}${node_id}ns"
    local env_source=$(get_nso_env_source)
    
    log_debug "Stopping NSO node $node_id"
    
    # Try to stop gracefully first (with environment sourced, as local user)
    local user_name=$(id -un)
    execute_cmd "sudo ip netns exec $netns sudo -u $user_name bash -c '
        $env_source
        export NCS_IPC_ADDR=$node_ip
        ncs --stop
    '" || true
    
    # Wait a moment for graceful shutdown
    sleep 2
    
    # Force kill if still running
    local pids=$(sudo ip netns exec "$netns" pgrep -f "beam.*ncs" 2>/dev/null || true)
    if [[ -n "$pids" ]]; then
        log_debug "Force killing NSO processes for node $node_id: $pids"
        echo "$pids" | xargs -r sudo ip netns exec "$netns" kill -9 || true
    fi
}

# Stop NSO nodes
stop_nso_nodes() {
    log_info "Stopping NSO nodes..."
    
    local stop_failed=false
    
    if [[ -n "$SPECIFIC_NODE" ]]; then
        if ! stop_nso_node "$SPECIFIC_NODE"; then
            log_warn "Failed to stop NSO node $SPECIFIC_NODE"
            stop_failed=true
        else
            log_info "Stopped NSO node $SPECIFIC_NODE"
        fi
    else
        for ((i=1; i<=NODES; i++)); do
            if ! stop_nso_node "$i"; then
                log_warn "Failed to stop NSO node $i"
                stop_failed=true
            fi
        done
        
        if [[ "$stop_failed" == "true" ]]; then
            log_warn "Some NSO nodes failed to stop cleanly"
        else
            log_info "All NSO nodes stopped"
        fi
    fi
    
    return 0  # Always return success to allow cleanup to continue
}

# Cleanup NSO configuration directories
cleanup_nso_nodes() {
    if [[ "$KEEP_CONFIG" == "true" ]]; then
        log_info "Keeping NSO configuration directories"
        return 0
    fi
    
    log_info "Cleaning up NSO configuration directories..."
    
    local cleanup_failed=false
    
    for ((i=1; i<=NODES; i++)); do
        local node_dir="${WORK_DIR}/ncs-run${i}"
        if [[ -d "$node_dir" ]]; then
            if ! execute_cmd "rm -rf $node_dir"; then
