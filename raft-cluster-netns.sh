# Isolate a single node from the cluster (simulate cable disconnect)
isolate_node() {
    local node_id="$1"
    local netns="${PREFIX}${node_id}ns"
    local veth_b="${PREFIX}${node_id}b"
    
    if ! ip netns list | grep -q "^$netns"; then
        log_error "Namespace $netns does not exist"
        return 1
    fi
    
    if ! ip link show "$veth_b" >/dev/null 2>&1; then
        log_error "Interface $veth_b does not exist"
        return 1
    fi
    
    log_info "Isolating node $node_id from cluster (simulating cable disconnect)"
    
    # Method 1: Bring down the bridge-side interface
    log_debug "Bringing down interface $veth_b"
    execute_cmd "sudo ip link set $veth_b down"
    
    # Method 2: Remove from bridge (but keep interface up for later healing)
    log_debug "Removing $veth_b from bridge $BRIDGE_NAME"
    execute_cmd "sudo ip link set $veth_b nomaster"
    
    log_info "Node $node_id is now isolated from the cluster"
    log_info "Use 'heal $node_id' or 'heal' to restore connectivity"
}

# Create a network partition between groups of nodes
create_partition() {
    local partition_spec="$1"
    
    if [[ ! "$partition_spec" =~ ^[0-9,]+$ ]]; then
        log_error "Invalid partition specification. Use format like '1,2' to isolate nodes 1 and 2"
        return 1
    fi
    
    # Parse the partition specification
    IFS=',' read -ra partition_nodes <<< "$partition_spec"
    
    # Validate node IDs
    for node_id in "${partition_nodes[@]}"; do
        if [[ ! "$node_id" =~ ^[0-9]+$ ]] || [[ "$node_id" -lt 1 ]] || [[ "$node_id" -gt "$NODES" ]]; then
            log_error "Invalid node ID: $node_id (must be between 1 and $NODES)"
            return 1
        fi
    done
    
    log_info "Creating network partition: nodes [${partition_spec}] vs remaining nodes"
    
    # Create a temporary bridge for the partitioned nodes
    local partition_bridge="${BRIDGE_NAME}part"
    log_debug "Creating partition bridge: $partition_bridge"
    execute_cmd "sudo ip link add dev $partition_bridge type bridge"
    execute_cmd "sudo ip addr add ${NETWORK_PREFIX}.99.254/16 dev $partition_bridge"
    execute_cmd "sudo ip link set dev $partition_bridge up"
    
    # Move specified nodes to the partition bridge
    for node_id in "${partition_nodes[@]}"; do
        local veth_b="${PREFIX}${node_id}b"
        local netns="${PREFIX}${node_id}ns"
        
        if ! ip netns list | grep -q "^$netns"; then
            log_warn "Namespace $netns does not exist, skipping node $node_id"
            continue
        fi
        
        if ! ip link show "$veth_b" >/dev/null 2>&1; then
            log_warn "Interface $veth_b does not exist, skipping node $node_id"
            continue
        fi
        
        log_debug "Moving node $node_id to partition bridge"
        execute_cmd "sudo ip link set $veth_b nomaster"
        execute_cmd "sudo ip link set $veth_b master $partition_bridge"
        execute_cmd "sudo ip link set $veth_b up"
    done
    
    log_info "Network partition created successfully"
    log_info "Partitioned nodes: [${partition_spec}]"
    log_info "Main cluster nodes: [$(get_remaining_nodes "$partition_spec")]"
    log_info "Use 'heal' to restore full connectivity"
}

# Get list of nodes not in the partition
get_remaining_nodes() {
    local partition_spec="$1"
    IFS=',' read -ra partition_nodes <<< "$partition_spec"
    
    local remaining=()
    for ((i=1; i<=NODES; i++)); do
        local is_partitioned=false
        for partitioned_node in "${partition_nodes[@]}"; do
            if [[ "$i" == "$partitioned_node" ]]; then
                is_partitioned=true
                break
            fi
        done
        if [[ "$is_partitioned" == "false" ]]; then
            remaining+=("$i")
        fi
    done
    
    # Join array with commas
    local IFS=','
    echo "${remaining[*]}"
}

# Heal network partition (restore connectivity)
heal_partition() {
    local specific_node="$1"
    
    if [[ -n "$specific_node" ]]; then
        log_info "Healing connectivity for node $specific_node"
        heal_single_node "$specific_node"
    else
        log_info "Healing all network partitions"
        heal_all_nodes
    fi
}

# Heal a specific node
heal_single_node() {
    local node_id="$1"
    local netns="${PREFIX}${node_id}ns"
    local veth_b="${PREFIX}${node_id}b"
    
    if ! ip netns list | grep -q "^$netns"; then
        log_error "Namespace $netns does not exist"
        return 1
    fi
    
    if ! ip link show "$veth_b" >/dev/null 2>&1; then
        log_error "Interface $veth_b does not exist"
        return 1
    fi
    
    log_debug "Restoring connectivity for node $node_id"
    
    # Remove from any partition bridge
    execute_cmd "sudo ip link set $veth_b nomaster" 2>/dev/null || true
    
    # Add back to main bridge
    execute_cmd "sudo ip link set $veth_b master $BRIDGE_NAME"
    execute_cmd "sudo ip link set $veth_b up"
    
    log_info "Node $node_id connectivity restored"
}

# Heal all nodes and remove partition bridges
heal_all_nodes() {
    # First, restore all nodes to main bridge
    for ((i=1; i<=NODES; i++)); do
        local veth_b="${PREFIX}${i}b"
        if ip link show "$veth_b" >/dev/null 2>&1; then
            log_debug "Restoring node $i to main bridge"
            execute_cmd "sudo ip link set $veth_b nomaster" 2>/dev/null || true
            execute_cmd "sudo ip link set $veth_b master $BRIDGE_NAME"
            execute_cmd "sudo ip link set $veth_b up"
        fi
    done
    
    # Remove any partition bridges
    local partition_bridge="${BRIDGE_NAME}part"
    if ip link show "$partition_bridge" >/dev/null 2>&1; then
        log_debug "Removing partition bridge: $partition_bridge"
        execute_cmd "sudo ip link set dev $partition_bridge down"
        execute_cmd "sudo ip link del $partition_bridge"
    fi
    
    log_info "All network partitions healed - full connectivity restored"
}

# Test connectivity and show partition status
show_partition_status() {
    log_info "Network Partition Status:"
    echo ""
    
    # Show bridge status
    echo "  Bridges:"
    if ip link show "$BRIDGE_NAME" >/dev/null 2>&1; then
        local main_members=$(bridge link show | grep "$BRIDGE_NAME" | wc -l)
        echo "    Main bridge ($BRIDGE_NAME): $main_members members"
        bridge link show | grep "$BRIDGE_NAME" | while read -r line; do
            echo "      $line"
        done
    fi
    
    local partition_bridge="${BRIDGE_NAME}part"
    if ip link show "$partition_bridge" >/dev/null 2>&1; then
        local partition_members=$(bridge link show | grep "$partition_bridge" | wc -l)
        echo "    Partition bridge ($partition_bridge): $partition_members members"
        bridge link show | grep "$partition_bridge" | while read -r line; do
            echo "      $line"
        done
    fi
    
    echo ""
    echo "  Node Connectivity Matrix:"
    echo -n "    From\\To: "
    for ((j=1; j<=NODES; j++)); do
        printf "%8s" "Node$j"
    done
    echo ""

    # Test connectivity between all pairs
    for ((i=1; i<=NODES; i++)); do
        local netns_i="${PREFIX}${i}ns"
        printf "%12s" "Node$i:"
        
        for ((j=1; j<=NODES; j++)); do
            if [[ "$i" == "$j" ]]; then
                printf "%8s" "SELF"
            else
                local target_ip
                if [[ "$NETWORK_TYPE" == "l3bgp" || "$NETWORK_TYPE" == "tailf_hcc" ]]; then
                    # For L3BGP, get the actual node IP from configuration
                    target_ip=$(eval echo "\$NODE_${j}_IP")
                    if [[ -z "$target_ip" ]]; then
                        target_ip="${NETWORK_PREFIX}.${j}.1"  # fallback
                    fi
                else
                    target_ip="${NETWORK_PREFIX}.${j}.1"
                fi
                
                if sudo ip netns exec "$netns_i" ping -c 1 -W 1 "$target_ip" >/dev/null 2>&1; then
                    printf "%8s" "✓"
                else
                    printf "%8s" "✗"
                fi
            fi
        done
        echo ""
    done
}

