# Parse command line arguments
parse_args() {
    if [[ $# -eq 0 ]]; then
        show_usage
        exit 1
    fi
    
    local command="$1"
    shift
    
    # First pass: look for config file option
    local temp_args=("$@")
    for ((i=0; i<${#temp_args[@]}; i++)); do
        if [[ "${temp_args[i]}" == "-c" ]] || [[ "${temp_args[i]}" == "--config" ]]; then
            CONFIG_FILE="${temp_args[i+1]}"
            break
        fi
    done
    
    # Set default config file path if not specified
    if [[ -z "$CONFIG_FILE" ]]; then
        CONFIG_FILE="$DEFAULT_CONFIG_FILE"
    fi
    
    # Load configuration file if it exists
    if [[ -f "$CONFIG_FILE" ]]; then
        load_config_file "$CONFIG_FILE"
    fi
    
    # Second pass: parse all arguments (these will override config file)
    while [[ $# -gt 0 ]]; do
        case $1 in
            -n|--nodes)
                NODES="$2"
                shift 2
                ;;
            -p|--prefix)
                PREFIX="$2"
                shift 2
                ;;
            -d|--work-dir)
                WORK_DIR="$2"
                shift 2
                ;;
            -c|--config)
                # Already handled in first pass
                shift 2
                ;;
            --cluster-name)
                CLUSTER_NAME="$2"
                shift 2
                ;;
            --network-prefix)
                NETWORK_PREFIX="$2"
                shift 2
                ;;
            --bridge-name)
                BRIDGE_NAME="$2"
                shift 2
                ;;
            --ssl-enabled)
                SSL_ENABLED=true
                shift
                ;;
            --ssl-cert-dir)
                SSL_CERT_DIR="$2"
                shift 2
                ;;
            --ncs-flags)
                NCS_FLAGS="$2"
                shift 2
                ;;
            --no-nso)
                NO_NSO=true
                shift
                ;;
            --timeout)
                TIMEOUT="$2"
                shift 2
                ;;
            --keep-config)
                KEEP_CONFIG=true
                shift
                ;;
            -v|--verbose)
                VERBOSE=true
                shift
                ;;
            --dry-run)
                DRY_RUN=true
                shift
                ;;
            --force)
                FORCE=true
                shift
                ;;
            --auto)
                AUTO_GENERATE=true
                shift
                ;;
            --output)
                shift
                CONFIG_OUTPUT="$1"
                shift
                ;;
            --type)
                shift
                CONFIG_TYPE="$1"
                shift
                ;;
            -h|--help)
                show_usage
                exit 0
                ;;
            *)
                if [[ "$command" == "shell" ]] || [[ "$command" == "exec" ]]; then
                    SPECIFIC_NODE="$1"
                    shift
                    if [[ "$command" == "exec" ]] && [[ $# -gt 0 ]]; then
                        # Remaining args are the command to execute
                        EXEC_COMMAND="$*"
                        break
                    fi
                elif [[ "$command" == "start" ]] || [[ "$command" == "stop" ]] || [[ "$command" == "isolate" ]] || [[ "$command" == "heal" ]]; then
                    # Optional node ID for these commands
                    if [[ "$1" =~ ^[0-9,]+$ ]]; then
                        SPECIFIC_NODE="$1"
                        shift
                    else
                        log_error "Unknown option: $1"
                        exit 1
                    fi
                elif [[ "$command" == "partition" ]]; then
                    # Partition specification required
                    if [[ "$1" =~ ^[0-9,]+$ ]]; then
                        SPECIFIC_NODE="$1"
                        shift
                    else
                        log_error "Invalid partition specification: $1"
                        exit 1
                    fi
                else
                    log_error "Unknown option: $1"
                    exit 1
                fi
                ;;
        esac
    done
    
    # Validate arguments
    if ! [[ "$NODES" =~ ^[0-9]+$ ]] || [[ "$NODES" -lt 1 ]]; then
        log_error "Invalid number of nodes: $NODES"
        exit 1
    fi
    
    # Validate prefix for security (no path traversal)
    if [[ "$PREFIX" =~ \.\.|/ ]]; then
        log_error "Invalid prefix: $PREFIX (cannot contain '..' or '/')"
        exit 1
    fi
    
    # Validate prefix characters (alphanumeric and basic symbols only)
    if ! [[ "$PREFIX" =~ ^[a-zA-Z0-9_-]+$ ]]; then
        log_error "Invalid prefix: $PREFIX (only alphanumeric, underscore, and hyphen allowed)"
        exit 1
    fi
    
    # Create work directory if it doesn't exist
    if [[ ! -d "$WORK_DIR" ]]; then
        mkdir -p "$WORK_DIR"
    fi
    
    WORK_DIR="$(cd "$WORK_DIR" && pwd)"  # Convert to absolute path
    
    # Execute command
    case "$command" in
        setup)
            setup_environment
            ;;
        start)
            start_nso_nodes
            ;;
        stop)
            stop_nso_nodes
            ;;
        cleanup)
            cleanup_environment
            ;;
        status)
            show_status
            ;;
        shell)
            if [[ -z "$SPECIFIC_NODE" ]]; then
                log_error "Node ID required for shell command"
                exit 1
            fi
            enter_namespace_shell "$SPECIFIC_NODE"
            ;;
        exec)
            if [[ -z "$SPECIFIC_NODE" ]] || [[ -z "${EXEC_COMMAND:-}" ]]; then
                log_error "Node ID and command required for exec"
                exit 1
            fi
            exec_in_namespace "$SPECIFIC_NODE" "$EXEC_COMMAND"
            ;;
        test)
            test_connectivity
            ;;
        isolate)
            if [[ -z "$SPECIFIC_NODE" ]]; then
                log_error "Node ID required for isolate command"
                exit 1
            fi
            isolate_node "$SPECIFIC_NODE"
            ;;
        partition)
            if [[ -z "$SPECIFIC_NODE" ]]; then
                log_error "Partition specification required (e.g., '1,2')"
                exit 1
            fi
            create_partition "$SPECIFIC_NODE"
            ;;
        heal)
            heal_partition "$SPECIFIC_NODE"
            ;;
        configure)
            run_configure_wizard
            ;;
        help)
            show_usage
            ;;
        *)
            log_error "Unknown command: $command"
            show_usage
            exit 1
            ;;
    esac
}

# Show current status
show_status() {
    log_info "Cluster Status:"
    echo "  Nodes: $NODES"
    echo "  Prefix: $PREFIX"
    echo "  Work Directory: $WORK_DIR"
    echo "  Cluster Name: $CLUSTER_NAME"
    echo "  Network Prefix: $NETWORK_PREFIX"
    echo "  Bridge Name: $BRIDGE_NAME"
    echo "  SSL Enabled: $SSL_ENABLED"
    echo ""
    
    # Check bridge status
    if ip link show "$BRIDGE_NAME" >/dev/null 2>&1; then
        echo -e "  Bridge: ${GREEN}UP${NC}"
        ip addr show "$BRIDGE_NAME" | grep -E "inet|UP"
    else
        echo -e "  Bridge: ${RED}DOWN${NC}"
    fi
    
    echo ""
    echo "  Namespaces:"
    for ((i=1; i<=NODES; i++)); do
        local netns="${PREFIX}${i}ns"
        if ip netns list | grep -q "^$netns"; then
            echo -e "    $netns: ${GREEN}EXISTS${NC}"
        else
            echo -e "    $netns: ${RED}MISSING${NC}"
        fi
    done
    
    echo ""
    show_nso_status
    
    echo ""
    show_partition_status
}

# Enter namespace shell
enter_namespace_shell() {
    local node_id="$1"
    local netns="${PREFIX}${node_id}ns"
    local env_source=$(get_nso_env_source)
    
    if ! ip netns list | grep -q "^$netns"; then
        log_error "Namespace $netns does not exist"
        exit 1
    fi
    
    log_info "Entering shell for namespace $netns"
    log_info "Use 'exit' to return to host shell"
    
    local user_name=$(id -un)
    local user_home=$(getent passwd "$user_name" | cut -d: -f6)
    local start_dir="$PWD"
    sudo ip netns exec "$netns" sudo -u "$user_name" bash --init-file <(echo "
        $env_source
        cd '$start_dir'
        export PS1='[$netns] \u@\h:\w$ '
        export NCS_IPC_ADDR=${NETWORK_PREFIX}.${node_id}.1
        echo 'Namespace: $netns'
        echo 'Node IP: ${NETWORK_PREFIX}.${node_id}.1'
        echo 'NSO Environment: ${ENV_SH_PATH:-not configured}'
        echo 'Available hosts:'
        cat /etc/hosts | grep ha-cluster
        echo 'NSO Commands available: ncs, ncs_cli, ncs_cmd'
    ")
}

# Execute command in namespace
exec_in_namespace() {
    local node_id="$1"
    local cmd="$2"
    local netns="${PREFIX}${node_id}ns"
    local env_source=$(get_nso_env_source)
    
    if ! ip netns list | grep -q "^$netns"; then
        log_error "Namespace $netns does not exist"
        exit 1
    fi
    
    log_debug "Executing in $netns: $cmd"
    
    local user_name=$(id -un)
    local start_dir="$PWD"
    sudo ip netns exec "$netns" sudo -u "$user_name" bash -c "
        $env_source
        cd '$start_dir'
        export NCS_IPC_ADDR=${NETWORK_PREFIX}.${node_id}.1
        $cmd
    "
}

