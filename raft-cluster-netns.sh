# Check prerequisites
check_prerequisites() {
    log_info "Checking prerequisites..."
    
    # Use basic prerequisite check from common.sh
    check_basic_prerequisites
    
    # Setup NSO environment if needed
    if [[ "$NO_NSO" != "true" ]]; then
        if ! setup_nso_environment; then
            log_error "Failed to setup NSO environment"
            exit 1
        fi
        
        # Source env.sh to check NSO commands
        local env_source=$(get_nso_env_source)
        if [[ -n "$env_source" ]]; then
            log_debug "Sourcing NSO environment: $ENV_SH_PATH"
            # Test if we can source the environment
            if ! bash -c "$env_source" >/dev/null 2>&1; then
                log_error "Failed to source NSO environment: $ENV_SH_PATH"
                exit 1
            fi
        fi
        
        # Check required NSO commands (after sourcing environment)
        local nso_commands=("ncs-setup" "ncs" "ncs_cmd")
        for cmd in "${nso_commands[@]}"; do
            if ! bash -c "$env_source; command -v $cmd" >/dev/null 2>&1; then
                log_error "Required NSO command not found: $cmd"
                if [[ -z "$env_source" ]]; then
                    log_error "Consider setting up env.sh path for NSO environment"
                fi
                exit 1
            fi
        done
    fi
    
    # Check additional system commands
    local required_commands=("iptables")
    for cmd in "${required_commands[@]}"; do
        if ! command_exists "$cmd"; then
            log_error "Required command not found: $cmd"
            exit 1
        fi
    done
    
    # Check NSO environment (only if env.sh is available)
    if [[ "$NO_NSO" != "true" ]] && [[ -n "$ENV_SH_PATH" ]]; then
        local env_source=$(get_nso_env_source)
        local ncs_dir=$(bash -c "$env_source; echo \${NCS_DIR:-}")
        
        if [[ -z "$ncs_dir" ]]; then
            log_error "NCS_DIR environment variable not set after sourcing $ENV_SH_PATH"
            exit 1
        fi
        
        if [[ ! -d "$ncs_dir" ]]; then
            log_error "NCS_DIR directory does not exist: $ncs_dir"
            exit 1
        fi
    fi
    
    log_info "Prerequisites check passed"
}

